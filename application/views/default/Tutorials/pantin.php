	<p class="text-justify">The various agents currently used for treatment of GERD include Mucoprotective substances, Antacids, H Blockers,  Prokinetics  PPIs.</p>
	<h4><strong>3.  LARYNGOPHARYNGEAL REFLUX (LPR):</strong></h4>
	<p class="text-justify">Laryngopharyngeal reflux (LPR) is the backflow of stomach contents up the esophagus and into the  throat.</p>
	<p class="text-justify">There are two sphincter muscles located in the esophagus: The lower esophageal sphincter (LES) and the upper esophageal sphincter (UES). When the lower esophageal sphincter is not functioning properly, there is a backflow of stomach acid into the esophagus. This condition is called as GERD (gastro esophageal reflux disease).</p>
	<p class="text-justify">As with the lower esophageal sphincter, if the upper esophageal sphincter doesn’t function properly, acid that has back flowed into the esophagus is allowed into the throat and larynx. When this happens, it’s called Laryngopharyngeal Reflux(LPR).</p>
	<p class="text-justify">LPR is different than gastro esophageal reflux disease(GERD) as Patients with GERD are usually suffer from heartburn and esophagitis while the patients with LPR do suffer from heartburn  or  esophagitis  (12%).</p>
	<ul>
		<h5><b>Symptoms:</b></h5>
		<li>Hoarseness</li>
		<li>Frequent throat clearing</li>
		<li>Globus  Pharyngeus  (sensation  of  a  lump  in  the  throat)</li>
		<li>Chronic Cough</li>
		<li>Bad/bitter taste in mouth</li>
		<li>Sore throat</li>
	</ul>
	<ul>
		<h5><b>Treatment:</b></h5>
		<li>Life style changes</li>
		<li>Diet modifications</li>
		<li>Medications: To reduce stomach acid or to promote normal motility. These can include Proton Pump Inhibitors, Histamine Receptor Antagonists and Prokinetic agents.</li>
		<li>Surgery to prevent reflux</li>
	</ul>
	<h3 class="text-center"><strong>MID-GUTMOTILITYDISORDERS</strong></h3>
	<h4><strong>GASTROPARESIS:</strong></h4>
		<p class="text-justify">Gastroparesis means paralysis of the muscles of the stomach which results in delayed emptying  of  the  food  from  the  stomach  to  the  small  intestine.</p>
		<p class="text-justify">Gastroparesis is more common in diabetic patients because of the damage of the nerves which generally control the stomach muscles. So it is also called as <b>Diabetic Gastroparesis</b></p>
		<p class="text-justify">Ordinarily, strong muscular contractions propel food on its long and convoluted journey through the digestive tract. But in gastroparesis, the muscles in the wall of the stomach work poorly or not at all, preventing the stomach from emptying properly. This can interfere with digestion, cause nausea and vomiting, and play havoc with blood sugar levels and nutrition</p>
	<h5><strong>Treatment</strong></h5>
	<p class="text-justify">Controlling diabetes or any other underlying condition that may be causing gastroparesis is the first step in treating the condition. Beyond this, dietary changes and medications that increase stomach contractions have long been the mainstay of gastroparesis therapy.</p>
	<p class="text-justify">Doctors usually prescribe two types of drugs to treat gastroparesis – anti-emetics which help control nausea and vomiting and prokinetics, which stimulate contractions of the stomach  muscles..</p>
	<h3 class="text-center"><strong>LOWER-GUTMOTILITYDISORDERS</strong></h3>
	<h4><strong>IRRITABLE BOWEL SYNDROME (IBS) WITH CONSTIPATION:</strong></h4>
	<p class="text-justify">Irritable bowel syndrome (IBS) is a chronic gastrointestinal disorder of unknown cause. Common symptoms include abdominal cramping or pain, bloating and gassiness and altered bowel habits. Irritable bowel syndrome has also been called spastic colon, functional bowel disease and mucous colitis.</p>
	<p class="text-justify">Irritable bowel syndrome is not contagious, inherited, or cancerous.It is estimated that 20% of population  have  symptoms  of  IBS.  It  occurs  more  often  in  women than in men and  the  onset  occurs  before  the  age  of  35  in  about  half  of  the  cases.</p>
	<p class="text-justify">Chronic <b>constipation</b> caused by motility disturbances of the lower digestive tract, for many begin in childhood and persists into adulthood. <b>Constipation</b> for those with digestive motility problems is much more than a mere nuisance.</p>
