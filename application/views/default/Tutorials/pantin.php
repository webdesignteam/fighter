		<h4><strong>MECHANISM OF ACTION:</strong></h4>
		<h5><strong>As a Prokinetic:</strong></h5>
		<p class="text-justify">The prokinetic effect of Levosulpiride is mediated through the blockade of enteric(neuronal and muscular)inhibitory dopamine D2 receptors.It also acts as a moderate agonist at the 5-HT<sub>4</sub> receptor. The serotonergic (5-HT) component of Levosulpiride enhances its therapeutic efficacy in gastrointestinal disorders.This property together  with antagonism at D2  receptors,  may  contribute  to  its  gastrointestinal  prokinetic  effect.</p>
		<h5><strong>As an Antiemetic:</strong></h5>
		<p class="text-justify">The anti emetic effect of Levosulpiride is due to inhibition of dopamine transmission and antagonism with D2 receptors of the neurons in the area postrema of the vomiting center (IV ventricle) or chemoreceptor trigger zone in the CNS, blocking the inhibitory effect of dopamine on cholinergic neurons and therefore permitting a sustained cholinergic induced contraction of smooth muscle cell in the myenteric plexus of the esophagus, stomach  and  intestine.</p>
