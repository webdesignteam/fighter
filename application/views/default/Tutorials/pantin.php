  <center><h4 class="text-center"><strong>GASTROINTESTINAL MOTILITY</strong></h4>  </center>
    <div class="col-sm-12">
    <p class="text-justify">GI motility refers to peristalsis, the smooth muscle contractions that propel digestive contents along the GI tract. Peristaltic motor activity of the gut is an essential activity for digestion and absorption of nutrients to sustain life. These rhythms are critical during and after meals to maximize contact of nutrients with villous surface area for absorption.</p>
	<p class="text-justify">Motility is controlled by the enteric nervous system(ENS),or “gutbrain”,a network of over 100million neurons embedded in the lining of the GI tract. Although the ENS is influenced by the central brain, it functions independently as a branch of the autonomic nervous system. The neurons within the ENS communicate using many of the neurotransmitters found in the brain; however, their local roles in the GI tract are to regulate smooth muscle activity. <b>The most important regulators of digestive motility are Serotonin, Acetylcholine  &  Dopamine.</b></p>
	<p class="text-justify">Dopamine decreases the Acetylcholine level while Seroton inincreases Acetylcholine level, therefore <b>dopaminergic and serotonergic activity balances the gut motility.</b></p>
	<p class="text-justify">Dopamine, while acting through specific dopaminergic receptors, inhibits lower esophageal sphincter(LES) pressure and gastroduodenal motility observed from the esophagus to the  colon.</p>
	<p class="text-justify"><b>Serotonin</b>  is a  multifarious  neurotransmitter,  with  many  functions. Excess  amounts of serotonin cause relaxation and sedation. Chemical name of serotonin is 5-hydroxytryptamine (5-HT).Serotonin is found to have a positive effect in the motor functions of the GI tract. Over 95% of the body’s total serotonin is generated by the GI tract, where it coordinates motility by binding to serotonin type 4 (5 HT) receptors. Once activated, these receptors stimulate the release of acetylcholine in tone uromuscular junctions, resulting in coordinated peristalsis, vascular tone and secretion. Serotonin dysfunction is involved in the pathophysiology of a number of functional GI disorders, such as Chronic Constipation, Irritable Bowel Syndrome, Diabetic Gastroparesis, Functional Dyspepsia and GERD. 5-HTP supports coordinated motility of the upper and lower GI tract,  in  part  by  supporting  5-HT<sub>4</sub> activity</p>
	<p class="text-justify"><b>In  the  GI  system  dopamine  delays  gastric  emptying  &  esophageal  motility  as it inhibits acetylcholine.</b></p>
	<h3 class="text-center"><strong>UPPER-GUTMOTILITYDISORDERS</strong></h3>
	<h4><strong>1. FUNCTIONAL DYSPEPSIA:</strong></h4>
	<p class="text-justify">Dyspepsia is a group of symptoms of the epigastric region including bloating, fullness, early satiety, nausea, belching, vomiting, acid reflux and hyperacidity.</p>
	<p class="text-justify">Dyspepsia can be caused by stress, spicy food & conditions which aggravate excess acid secretion etc.</p>
	<p class="text-justify">It is clear that dyspepsia and functional dyspepsia in particular are common conditions globally, affecting most populations, regardless of location. Risk factors for functional dyspepsia have been shown to include females and underlying psychological disturbances, whilst environmental / lifestyle habits such as poor socio-economic status, smoking, increased caffeine intake and ingestion of non-steroidal anti-inflammatory drugs appear to be more relevant to uninvestigated dyspepsia.</p>
	<h4><strong>2.  GERD:</strong></h4>
	<h5><strong>Gastro-Esophageal Reflux Disease (GERD)</strong></h5>
	<p class="text-justify">GERD is defined as chronic symptom or mucosal damage produced by the abnormal reflux of gastric contents into the esophagus. Reflux esophagitis refers to a subgroup of GERD patients with histopathologically demonstrated characteristic changes in the esophageal mucosa.</strong></p>
