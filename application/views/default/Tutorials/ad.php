  <h2 class="text-center"><strong>BASICS OF DIARRHOEA</strong></h2>  
    <div class="col-sm-12">
	<h4><strong>INTRODUCTION</strong></h4>
    <p class="text-justify">Diarrhoea is one of the most common diagnoses in clinical practice. It affects all age groups. Most of the deaths associated with diarrheal illness occur in the very young and the elderly populations, whose health may be put at risk from a moderate amount of dehydration.</p>
	<p class="text-justify">According to the World Health Organization, two million children die each year in developing countries from diarrhea. This makes diarrhoea the second most serious killer of children under five, worldwide.</p>
	<p class="text-justify">Diarrhoea can however in most cases be prevented or treated. Correct management of Diarrhoea could save the lives of up to 90% of children and many adults, who currently die from the effects of the disease.</p>
	<h4><strong>DIARRHOEA</strong></h4>
	<h4><strong>Normal bowel frequency</strong></h4>
	<p class="text-justify">For most individuals, normal daily stool weight ranges between 100 and 200grams. Normal bowel frequency ranges from 3 times a day to 3 times a week.</p>
	<h4><strong>Diarrhoea</strong></h4>
	<p class="text-justify">Diarrhoea is defined as an increase in daily stool weight above 200grams.In clinical practice, the term is used to describe increased liquidity of the stools, usually associated with increased stool weight and frequency (more than 3 times per day)</p>
	<h4><strong>Different types of diarrhoea</strong></h4>
	<p class="text-justify">In clinical practice, duration of the illness and stool characteristics are most useful in the evaluation and treatment of patients with Diarrhoea</p>
	<h4><strong>Acute diarrhoea</strong></h4>
	<p class="text-justify">Acute diarrhoea is defined as a diarrhoeal illness lasting for less than 4weeks.</p>
	<h4><strong>Chronic diarrhoea</strong></h4>
	<p class="text-justify">If the illness  persists  for  more  than  4  weeks,  it  is  considered chronic  diarrhoea.</p>
	<h4><strong>There are 4 mechanisms of diarrhoea:</strong></h4>
		<ol>		
		<li><strong>Osmotic  Diarrhoea</strong></li>
		<p class="text-justify">This type of diarrhoea is due to the presence of an unabsorbable or poorly absorbable solute that exerts an osmotic pressure effect across the intestinal mucosa, resulting in excessive water output. Because the diarrhoea is caused by the solute, it tends to stop  during  fasting.</p>
		<li><strong>Secretory Diarrhoea</strong></li>
		<p class="text-justify">In this type of diarrhoea there is abnormal ion transport across the intestinal epithelial cells, which results in increased secretion, decreased absorption, or both. As the diarrhoea is not related to intestinal contents, it typically does not stop with fasting.A classic example of acute secretory diarrhoea is cholera</p>
		<li><strong>Altered Motility</strong></li>
		<p class="text-justify">Abnormal motility (increased/decreased) can cause diarrhoea.</p> 
		<li><strong>Exudative  Diarrhoea</strong></li>
		<p class="text-justify">Extensive injury of the small bowel or colon mucosa may result in fluid and protein loss into the intestinal lumen and ensuring diarrhoea.</p>
		</ol>
		<p><b>It is important to note that more than one mechanism may coexist. For example, in infectious and inflammatory conditions, malabsorption leading to osmotic diarrhoea  and  active  secretion  can  coexist.</b></p>
		<h4><strong>Diarrhoea and the enteric nervous system</strong></h4>
		<p class="text-justify">The pathophysiological mechanisms underlying the fluidlosses seen in different types of diarrhoea have been debated for decades. However, during the last three decades it has been become increasingly evident that disturbances in the epithelial transport are often one of  the major causes of intestinal fluidloss,  although  motility  may  contribute,  at  least  in  some  types  of  diarrhoea.</p>
		<p class="text-justify">Studies on conscious neonatal mice with rotavirus diarrhoea showed that nerve blockers decreased diarrhoea. Subsequent studies invitro confirmed that neurotransmitter blockers inhibit fluid secretion in the intestine. Opiates have been known to inhibit intestinal fluid and electrolyte secretion as well as gut motility. The endogenous opiates, enkephalins, are found in enteric nerves as neurotransmitters. Studies demonstrated that these enkephalins reduce fluid secretion in the gut. Inhibitors of the enzymes encephalinases, which degrade the enkephalins, cause a build up of the enkephalins and enhance their action. In line with this the enkephalinase inhibitor racecadotril has been shown to attenuate acute diarrhoea in children and adults. Salazar- Lindoetal. Have recently shown in a clinical trail that racecadotril markedly inhibited stool output in young Peruvian children with rotavirus  diarrhoea.</p>
		<h4><strong>Enkephalins</strong></h4>
		<p class="text-justify">Enkephalins occur in the brain and spinal cord and also in the gastrointestinal tract. They have potent opiate -like  effects  and  probably  serve  as  neurotransmitters.</p>
		<h4><strong>Role of Enkephalinase</strong></h4>
		<p class="text-justify">Enkephalinase is a cell membrane peptidase enzyme located in various tissues, notably the epithelium of the small intestine. This enzyme contributes both to the digestion of exogenous peptides and to the breakdown of endogenous peptides such as enkephalins, neurokinin and substanceP.</p>
		<h4><strong>Symptoms and signs of diarrhoea</strong></h4>
		<p class="text-justify">Patients with diarrhoea, present with various clinical features depending on the underlyingcause.</p>
		<p class="text-justify">Diarrhoea due to small-intestinal disease is typically high-volume, watery, and often associated with malabsorption and dehydration is frequent. Diarrhoea due to colonic involvement is more often associated with frequent small-volume stools, with the presence of blood  and  a  sensation  of  urgency.</p>
		<p class="text-justify">Patients with acute infectious diarrhoea typically present with nausea, vomiting, abdominal pain, fever, and frequent stools, which may be watery, malabsorptive or bloody depending on  the specific pathogen.</p>
		<h4><strong>Treatment for diarrhoea</strong></h4>
		<p class="text-justify">The principal components of the treatment of acute diarrhoea are fluid and electrolyte replacement,  dietary  modifications  and  drug  therapy.</p>
		<h4><strong>Rehydration</strong></h4>
		<p class="text-justify">If patients are otherwise healthy and do not have dehydration, adequate fluid intake can be achieved with soft drinks, fruit juice, broth, soup and salted biscuits. In those with excessive fluid losses and dehydration, intravenous fluids or oral rehydration therapy with isotonic, electrolyte solutions containing glucose or starch should be given.</p>
		<h4><strong>Antidiarrhoeal Agents</strong></h4>
		<p class="text-justify">These can be very useful in amelioration of symptoms and include opioid derivatives like loperamide, diphenoxylate-atropine and tincture of opium. Bismuth subsalicylate is another useful agent, but it is contraindicated in HIV-infected patients since it may cause bismuth encephalopathy.</p>
		<h4><strong>Antimicrobial Therapy</strong></h4>
		<p class="text-justify">Because the majority of patients have mild, self-limited disease due to viruses or non invasive bacteria, empiric treatment of all patients is not warranted.</p>
