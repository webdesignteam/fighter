           <p class="text-justify"><strong>Mechanism of Action</strong></p>
           <p class="text-justify">Pantoprazole is a proton pump inhibitor (PPI) that suppresses the final step in gastric acid production by forming a covalent bond to two sites of the H+ /K+ ATPase enzyme system at the secretory surface of the gastric parietal cell. This effect is dose-related and leads to inhibition of both basal and stimulated gastric acid secretion irrespective of the stimulus. The binding to the H+ /K+ ATPase results antisecretory effect that persists longer than 24 hours.</p>
